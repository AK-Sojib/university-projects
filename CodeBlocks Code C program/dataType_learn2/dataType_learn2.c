/*Write a program that prints a floating,
double number and character?
*/

#include<stdio.h>
int main()
{
  float num1=10.5;
  double num2=10.25252525;
  char ch='a';
  printf("num1 =%.1f\n",num1);//dosomik ar por jodi sudu 1 ti sonkha dekte cai tahole %f ar jaigai %.1f dite hobe.
  printf("num2 =%lf\n",num2);
  printf("ch =%c",ch);
  getch();
}
